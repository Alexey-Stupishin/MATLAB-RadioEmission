function [T, Y, lambda] = getLowLouPNFromFile(fname, n, nlambda)

fin = fopen(fname, 'r');

% 1 8 10000
% 0.000000e+00 4.274080e-01 2.543240e+00 8.760050e+00 2.286830e+01 5.023530e+01 9.798140e+01 1.751400e+02 
% 1 -1.000000e+00 0.000000e+00 1.000000e+01 0.000000e+00 1.000000e+01 0.000000e+00 1.000000e+01 0.000000e+00 1.000000e+01 0.000000e+00 1.000000e+01 0.000000e+00 1.000000e+01 0.000000e+00 1.000000e+01 0.000000e+00 1.000000e+01 
% 2 -9.998000e-01 2.000000e-03 9.998000e+00 2.000000e-03 9.998000e+00 2.000000e-03 9.998000e+00 2.000000e-03 9.998000e+00 2.000000e-03 9.998000e+00 2.000000e-03 9.998000e+00 2.000000e-03 9.998000e+00 2.000000e-03 9.997999e+00 
% 3 ......

s = fgets(fin);
v = sscanf(s, '%d %d %d');
assert(v(1) == n);
assert(v(2)-1 >= nlambda && nlambda >= 0);

s = fgets(fin);
ll = sscanf(s, '%f');
lambda = ll(nlambda+1);

T = zeros(v(3), 1);
Y = zeros(v(3), 2);
for k = 1:v(3)
    s = fgets(fin);
    vs = sscanf(s, '%e');
    T(k) = vs(2);
    Y(k, 1) = vs(2*nlambda + 3);
    Y(k, 2) = vs(2*nlambda + 4);
end

fclose(fin);

end
